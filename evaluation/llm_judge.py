"""
evaluation/llm_judge.py — Blind LLM-as-Judge evaluation.

The LLM judge evaluates each answer on three dimensions:
  1. Factual consistency  — Is the answer factually consistent with the source text?
  2. Question relevance   — Does the answer actually address the question?
  3. Citation accuracy    — Are the sources/papers correctly referenced?

"Blind" evaluation means the judge sees the question, answer, and source context,
but NOT which architecture produced the answer. This prevents the judge from
being biased toward architectures it might perceive as "more sophisticated".

We use a *stronger* judge model (configured via JUDGE_MODEL) than the model
used for answer generation. This matters because we want the judge to catch
subtle factual errors that a weaker model might miss.

All scores are integers 1-5, consistent with the Likert-style rubric below.
"""
import json
import logging
import re
import sys
from pathlib import Path
from typing import List, Dict, Any

sys.path.insert(0, str(Path(__file__).parent.parent))
import config
from utils.llm_client import LLMClient

logger = logging.getLogger(__name__)

# Instantiate judge as a module-level singleton to avoid repeated initialisation
# (model loading is cheap for API clients, but we avoid creating many objects)
_judge_llm: LLMClient = None


def get_judge() -> LLMClient:
    """Return the judge LLM, creating it once on first call."""
    global _judge_llm
    if _judge_llm is None:
        _judge_llm = LLMClient(model=config.JUDGE_MODEL)
    return _judge_llm


# ─────────────────────────────────────────────────────────────────────────────
# Judge prompt
# ─────────────────────────────────────────────────────────────────────────────

_JUDGE_SYSTEM = """You are an impartial scientific evaluator assessing the quality
of answers generated by AI systems in response to research paper questions.
You will be shown a question, a set of source passages, and an AI-generated answer.
Your job is to evaluate the answer fairly and precisely."""

_JUDGE_PROMPT = """Evaluate the following AI-generated answer to a research paper question.

=== SOURCE CONTEXT (ground truth reference material) ===
{context}

=== QUESTION ===
{question}

=== AI-GENERATED ANSWER ===
{answer}

=== GROUND TRUTH ANSWER ===
{ground_truth}

Score the answer on three dimensions. Use this rubric:

FACTUAL CONSISTENCY (1-5):
  1 = Answer contains multiple factual errors or fabrications not in source
  2 = Answer has significant inaccuracies or unsupported claims
  3 = Answer is mostly accurate with minor issues
  4 = Answer is accurate with very minor issues
  5 = Answer is fully accurate and consistent with source context

QUESTION RELEVANCE (1-5):
  1 = Answer ignores the question entirely
  2 = Answer barely addresses the question
  3 = Answer partially addresses the question
  4 = Answer mostly addresses the question
  5 = Answer directly and completely addresses the question

CITATION ACCURACY (1-5):
  1 = Answer cites wrong papers or completely ignores sources
  2 = Answer has mostly incorrect or missing citations
  3 = Answer has some correct citations
  4 = Answer mostly cites correctly
  5 = Answer correctly cites or attributes all claims to the right sources

Return ONLY valid JSON, no other text:
{{
  "factuality": <1-5>,
  "relevance": <1-5>,
  "citation": <1-5>,
  "factuality_reason": "<one brief sentence>",
  "relevance_reason": "<one brief sentence>",
  "citation_reason": "<one brief sentence>"
}}"""


# ─────────────────────────────────────────────────────────────────────────────
# Evaluation function
# ─────────────────────────────────────────────────────────────────────────────

def judge(
    question: str,
    answer: str,
    contexts: List[str],
    ground_truth: str,
) -> Dict[str, Any]:
    """
    Ask the judge LLM to score one answer on factuality, relevance, and citation.

    Returns:
        {factuality: int, relevance: int, citation: int,
         factuality_reason: str, relevance_reason: str, citation_reason: str}

    Falls back to score=3 (neutral) for each dimension if the judge fails,
    so one bad API call doesn't crash the entire experiment run.
    """
    llm = get_judge()

    # Truncate contexts to avoid exceeding model context windows
    # We use only the top 3 context chunks for the judge to keep prompts manageable
    context_str = "\n\n---\n\n".join(
        f"[Passage {i+1}]\n{ctx[:600]}"
        for i, ctx in enumerate(contexts[:3])
    ) if contexts else "No context provided."

    prompt = _JUDGE_PROMPT.format(
        context=context_str,
        question=question,
        answer=answer[:1500],     # truncate very long answers
        ground_truth=ground_truth[:500],
    )

    try:
        raw = llm.complete(prompt, system=_JUDGE_SYSTEM, temperature=0.0, max_tokens=512)
        # Strip markdown code fences if present
        raw = re.sub(r"```(?:json)?|```", "", raw).strip()
        scores = json.loads(raw)

        # Clamp scores to valid range in case the LLM hallucinated out-of-range values
        for key in ["factuality", "relevance", "citation"]:
            scores[key] = max(1, min(5, int(scores.get(key, 3))))

        return scores

    except Exception as exc:
        logger.warning(f"Judge evaluation failed: {exc}. Returning neutral scores.")
        return {
            "factuality":        3,
            "relevance":         3,
            "citation":          3,
            "factuality_reason": "Evaluation failed — default score assigned.",
            "relevance_reason":  "Evaluation failed — default score assigned.",
            "citation_reason":   "Evaluation failed — default score assigned.",
        }
